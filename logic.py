a = 6
b = 4

print("----(==)----")
print(a == b)
print(a == 6)
print(4 == b)

print("----(!=)----")
print(a != b)
print(a != 6)
print(4 != b)

# Operator Logika
print("----(or)----")
print(False or False)
print(False or True)
print(True or False)
print(True or True)

print("\n----(and)----")
print(False and False)
print(False and True)
print(True and False)
print(True and True)

print("\n----(xor {^})----")
print(False ^ False)
print(False ^ True)
print(True ^ False)
print(True ^ True)